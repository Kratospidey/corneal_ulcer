import os
import json
import csv
from pathlib import Path
import torch
import numpy as np
from PIL import Image
import matplotlib.pyplot as plt
from tqdm import tqdm

from config_utils import resolve_config
from data.label_utils import get_task_definition
from model_factory import create_model, get_gradcam_target_layer
from explainability.gradcam_utils import GradCAM, disable_inplace_relu, overlay_cam_on_image
from inference.inference_utils import load_image_for_inference
from utils_io import safe_open_image

def generate_w0035_artifacts():
    RUN_NAME = "pattern3__convnextv2_tiny__cornea_crop_scale_v1__augplus_v2__weighted_sampler_tempered__officialinit_ordinalaux_w0035__holdout_v1__seed42"
    checkpoint_path = Path(f"models/exported/{RUN_NAME}/best.pt")
    config_path = Path(f"configs/model_improve/ordinal_weight_grid/train_pattern3_officialinit_ordinalaux_w0035.yaml")
    
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"Using device: {device}")
    
    config = resolve_config(config_path)
    task_config = resolve_config(config["task_config"])
    task_def = get_task_definition(task_config["task_name"])
    class_names = task_def.class_names
    
    output_root = Path("outputs")
    paper_figures_dir = output_root / "paper_figures" / RUN_NAME
    explainability_dir = output_root / "explainability" / RUN_NAME
    xai_gallery_dir = paper_figures_dir / "xai_gallery"
    xai_gallery_mirror = explainability_dir / "xai_gallery"
    
    xai_gallery_dir.mkdir(parents=True, exist_ok=True)
    xai_gallery_mirror.mkdir(parents=True, exist_ok=True)
    
    # Load model
    model = create_model(config["model"], num_classes=len(class_names)).to(device)
    checkpoint = torch.load(checkpoint_path, map_location=device)
    model.load_state_dict(checkpoint["model_state_dict"])
    disable_inplace_relu(model)
    model.eval()
    
    # We need predictions to select examples
    predictions_csv = output_root / "model_improve_2026-04-25" / "ordinal_weight_grid" / "predictions" / RUN_NAME / "test_predictions.csv"
    if not predictions_csv.exists():
        print(f"Predictions CSV not found at {predictions_csv}. Running evaluation...")
        # (This should have been generated by main_eval.py in Step 3, which we ran)
        # But if it wasn't, we can't proceed easily without re-running eval or similar.
        # Let's assume it exists because we ran main_eval.py.
        pass

    with open(predictions_csv, "r") as f:
        reader = csv.DictReader(f)
        prediction_rows = list(reader)
        
    print(f"Loaded {len(prediction_rows)} predictions.")
    
    # Select examples for XAI
    # 3 correct per class + all wrong
    selected_rows = []
    for cls in class_names:
        cls_correct = [r for r in prediction_rows if r["true_label"] == cls and r["correct"].lower() == "true"]
        cls_correct.sort(key=lambda x: float(x["confidence"]), reverse=True)
        selected_rows.extend(cls_correct[:3])
        
    wrong_rows = [r for r in prediction_rows if r["correct"].lower() == "false"]
    selected_rows.extend(wrong_rows)
    
    # Deduplicate by image_id
    seen_ids = set()
    unique_selected = []
    for r in selected_rows:
        if r["image_id"] not in seen_ids:
            unique_selected.append(r)
            seen_ids.add(r["image_id"])
            
    print(f"Selected {len(unique_selected)} images for XAI gallery.")
    
    camera = GradCAM(model, get_gradcam_target_layer(model, config["model"]["name"]))
    
    xai_manifest = []
    
    for row in tqdm(unique_selected, desc="Generating Grad-CAMs"):
        image_id = row["image_id"]
        true_label = row["true_label"]
        pred_label = row["pred_label"]
        confidence = float(row["confidence"])
        raw_image_path = row["raw_image_path"]
        
        image_tensor = load_image_for_inference(
            image_path=raw_image_path,
            preprocessing_mode=config.get("preprocessing_mode", "raw_rgb"),
            cornea_mask_path=row.get("cornea_mask_path"),
            image_size=config.get("image_size", 224)
        ).to(device)
        
        pred_idx = class_names.index(pred_label)
        cam_array = camera.generate(image_tensor, pred_idx)
        
        raw_image = safe_open_image(Path(raw_image_path))
        overlay = overlay_cam_on_image(raw_image, cam_array)
        
        # Save individual image
        save_name = f"{image_id}_gradcam_{pred_label}.png"
        save_path = xai_gallery_dir / save_name
        overlay.save(save_path)
        
        # Mirror
        overlay.save(xai_gallery_mirror / save_name)
        
        xai_manifest.append({
            "image_id": image_id,
            "true_label": true_label,
            "pred_label": pred_label,
            "confidence": confidence,
            "source_image_path": raw_image_path,
            "gradcam_path": str(save_path.relative_to(output_root))
        })
        
    camera.close()
    
    with open(xai_gallery_dir / "xai_manifest.json", "w") as f:
        json.dump(xai_manifest, f, indent=4)
        
    with open(xai_gallery_mirror / "xai_manifest.json", "w") as f:
        json.dump(xai_manifest, f, indent=4)
        
    print(f"XAI gallery generated in {xai_gallery_dir}")

if __name__ == "__main__":
    generate_w0035_artifacts()
